def _graupel_batch(batched_args, batch_dims):
    if all(bd is None for bd in batch_dims):
        return optimized_graupel_p.bind(*batched_args), (None,) * 13

    def unbatched_call(*args):
        return optimized_graupel_p.bind(*args)

    batched_fn = jax.vmap(unbatched_call, in_axes=batch_dims, out_axes=(0,) * 13)
    results = batched_fn(*batched_args)
    return results, (0,) * 13


batching.primitive_batchers[optimized_graupel_p] = _graupel_batch


