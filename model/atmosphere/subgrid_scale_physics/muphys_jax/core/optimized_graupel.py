_GRAUPEL_HLO_PATH: str | None = None
_USE_GRAUPEL_OPTIMIZED: bool = False
_CACHED_GRAUPEL_HLO: str | None = None
_MERGE_COUNTER = 0


def configure_optimized_graupel(hlo_path: str | None = None, use_optimized: bool = False):
    """Configure the optimized full graupel HLO injection."""
    global _GRAUPEL_HLO_PATH, _USE_GRAUPEL_OPTIMIZED, _CACHED_GRAUPEL_HLO
    _GRAUPEL_HLO_PATH = hlo_path
    _USE_GRAUPEL_OPTIMIZED = use_optimized
    _CACHED_GRAUPEL_HLO = None


def is_graupel_optimized_enabled() -> bool:
    return _USE_GRAUPEL_OPTIMIZED and _GRAUPEL_HLO_PATH is not None


