def _load_graupel_hlo():
    """Load the combined graupel HLO module."""
    global _CACHED_GRAUPEL_HLO
    if _CACHED_GRAUPEL_HLO is not None:
        return _CACHED_GRAUPEL_HLO

    if not _GRAUPEL_HLO_PATH:
        return None

    try:
        path = pathlib.Path(_GRAUPEL_HLO_PATH)
        with open(path) as f:
            _CACHED_GRAUPEL_HLO = f.read()
        print(
            f"[GRAUPEL HLO] Loaded from: {_GRAUPEL_HLO_PATH} ({len(_CACHED_GRAUPEL_HLO)/1024:.0f} KB)"
        )
        return _CACHED_GRAUPEL_HLO
    except Exception as e:
        print(f"[GRAUPEL HLO] Failed to load: {e}")
        return None


def _graupel_lowering(ctx, *args):
    """MLIR lowering: inject StableHLO or fall back to JAX. dt = args[-1]."""
    if is_graupel_optimized_enabled():
        hlo = _load_graupel_hlo()
        if hlo is not None:
            return _stablehlo_injection(ctx, *args)
    return _fallback_lowering(ctx, *args)


def _stablehlo_injection(ctx, *args):
    """Merge pre-compiled StableHLO into the MLIR module."""
    global _CACHED_GRAUPEL_HLO, _MERGE_COUNTER

    hlo_text = _CACHED_GRAUPEL_HLO
    if isinstance(hlo_text, bytes):
        hlo_text = hlo_text.decode("utf-8")

    avals_out = ctx.avals_out
    dst_module = ctx.module_context.module

    try:
        print(f"[GRAUPEL HLO] Parsing StableHLO ({len(hlo_text)} chars)...")
        src_module = ir.Module.parse(hlo_text)
        print("[GRAUPEL HLO] Parsed successfully")

        _MERGE_COUNTER += 1
        desired_name = f"_injected_graupel_{_MERGE_COUNTER}"

        print(f"[GRAUPEL HLO] Merging as '{desired_name}'...")
        actual_name = mlir.merge_mlir_modules(dst_module, desired_name, src_module)
        print(f"[GRAUPEL HLO] Merged as '{actual_name}'")

        from jax._src.lib.mlir.dialects import func as func_dialect

        result_types = [mlir.aval_to_ir_type(aval) for aval in avals_out]

        # dt (args[-1]) is baked into the StableHLO, don't pass it
        call_op = func_dialect.CallOp(
            result_types,
            ir.FlatSymbolRefAttr.get(actual_name),
            list(args[:-1]),
        )
        print("[GRAUPEL HLO] Injection successful!")
        return list(call_op.results)

    except Exception as e:
        print(f"[GRAUPEL HLO] Injection failed: {e}")
        import traceback

        traceback.print_exc()
        return _fallback_lowering(ctx, *args)


def _fallback_lowering(ctx, *args):
    """Inline the original JAX implementation. args[-1] = dt."""
    from ..implementations.graupel_native_transposed import (
        _precipitation_effects_native_transposed_jax,
    )
    from ..implementations.q_t_update_fused import q_t_update_fused

    def full_graupel(kmin_r, kmin_i, kmin_s, kmin_g, t, p, rho, dz, qv, qc, qr, qs, qi, qg, dt):
        q_in = Q(v=qv, c=qc, r=qr, s=qs, i=qi, g=qg)
        qnc = 100.0
        last_lev = t.shape[0] - 1

        q_updated, t_updated = q_t_update_fused(t, p, rho, q_in, dt, qnc)

        qr_out, qs_out, qi_out, qg_out, t_final, pflx, pr, ps, pi, pg, eflx = (
            _precipitation_effects_native_transposed_jax(
                last_lev, kmin_r, kmin_i, kmin_s, kmin_g, q_updated, t_updated, rho, dz, dt
            )
        )

        return (
            t_final,
            q_updated.v,
            q_updated.c,
            qr_out,
            qs_out,
            qi_out,
            qg_out,
            pflx,
            pr,
            ps,
            pi,
            pg,
            eflx,
        )

    lowering_fn = mlir.lower_fun(full_graupel, multiple_results=True)
    return lowering_fn(ctx, *args)


mlir.register_lowering(optimized_graupel_p, _graupel_lowering)


