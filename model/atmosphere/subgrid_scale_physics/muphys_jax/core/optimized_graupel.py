def auto_configure():
    hlo_path = os.environ.get("MUPHYS_GRAUPEL_HLO")
    use_opt = os.environ.get("MUPHYS_USE_GRAUPEL_OPTIMIZED", "0") == "1"

    if hlo_path and use_opt:
        configure_optimized_graupel(hlo_path=hlo_path, use_optimized=True)
        print(f"[GRAUPEL HLO] Auto-configured from: {hlo_path}")


auto_configure()
