optimized_graupel_p = Primitive("optimized_graupel_full")
optimized_graupel_p.multiple_results = True


def _graupel_abstract_eval(
    kmin_r, kmin_i, kmin_s, kmin_g, t, p, rho, dz, qv, qc, qr, qs, qi, qg, dt
):
    """Returns 13 f64 arrays of shape (nlev, ncells)."""
    nlev, ncells = t.shape
    f64_shape = jax_core.ShapedArray((nlev, ncells), jnp.float64)

    # 13 outputs: t_final, qv, qc, qr, qs, qi, qg, pflx, pr, ps, pi, pg, eflx
    return tuple([f64_shape] * 13)


def _graupel_impl(kmin_r, kmin_i, kmin_s, kmin_g, t, p, rho, dz, qv, qc, qr, qs, qi, qg, dt):
    """Eager-mode fallback: calls original JAX functions."""
    from ..implementations.graupel_native_transposed import (
        _precipitation_effects_native_transposed_jax,
    )
    from ..implementations.q_t_update_fused import q_t_update_fused

    q_in = Q(v=qv, c=qc, r=qr, s=qs, i=qi, g=qg)
    qnc = 100.0
    last_lev = t.shape[0] - 1

    # Phase 1: q_t_update
    q_updated, t_updated = q_t_update_fused(t, p, rho, q_in, dt, qnc)

    # Phase 2: precipitation_effects
    qr_out, qs_out, qi_out, qg_out, t_final, pflx, pr, ps, pi, pg, eflx = (
        _precipitation_effects_native_transposed_jax(
            last_lev, kmin_r, kmin_i, kmin_s, kmin_g, q_updated, t_updated, rho, dz, dt
        )
    )

    return (
        t_final,
        q_updated.v,
        q_updated.c,
        qr_out,
        qs_out,
        qi_out,
        qg_out,
        pflx,
        pr,
        ps,
        pi,
        pg,
        eflx,
    )


optimized_graupel_p.def_abstract_eval(_graupel_abstract_eval)
optimized_graupel_p.def_impl(_graupel_impl)


